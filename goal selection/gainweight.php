      <h2>Pull-Ups</h2>
      <h4>Instructions:</h4>
      <p>
        <strong>Starting Position:</strong> Hang from a pull-up bar with your hands slightly wider than shoulder-width, palms facing away from you.<br>
        <strong>Pulling Phase:</strong> Engage your back and arms to pull your body upward until your chin is above the bar.<br>
        <strong>Lowering Phase:</strong> Slowly lower yourself back to the starting position with control.<br>
        <strong>Repetitions:</strong> Aim for 3 sets of 5-10 repetitions, depending on your strength level.
