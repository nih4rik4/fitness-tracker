      <h2>Lunges</h2>
      <h4>Instructions:</h4>
      <p>
        <strong>Starting Position:</strong> Stand with your feet hip-width apart.<br>
        <strong>Stepping Phase:</strong> Step forward with one leg, lowering your hips until both knees are bent at about a 90-degree angle. The back knee should hover just above the ground.<br>
        <strong>Returning Phase:</strong> Push through the front heel to return to the starting position.<br>
        <strong>Repetitions:</strong> Aim for 3 sets of 10-12 repetitions per leg.
      </p>
