      <h2>Push-Ups</h2>
      <h4>Instructions:</h4>
      <p>
        <strong>Starting Position:</strong> Begin in a plank position with your hands placed slightly wider than shoulder-width apart and your feet together.<br>
        <strong>Lowering Phase:</strong> Bend your elbows to lower your body toward the floor while keeping your back straight. Aim to go until your chest almost touches the ground.<br>
        <strong>Pushing Phase:</strong> Press through your palms to push your body back up to the starting position. Ensure your core is engaged throughout the movement.<br>
        <strong>Repetitions:</strong> Aim for 3 sets of 8-12 repetitions.
      </p>
