      <h2>Squats</h2>
      <h4>Instructions:</h4>
      <p>
        <strong>Starting Position:</strong> Stand with your feet shoulder-width apart and your toes slightly pointed out.<br>
        <strong>Lowering Phase:</strong> Push your hips back and bend your knees to lower your body into a squat position, keeping your chest up and back straight.<br>
        <strong>Depth:</strong> Go as low as comfortable, ideally until your thighs are parallel to the ground.<br>
        <strong>Rising Phase:</strong> Press through your heels to stand back up to the starting position.<br>
        <strong>Repetitions:</strong> Aim for 3 sets of 10-15 repetitions.
      </p>
