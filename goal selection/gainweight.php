      <h2>Crunches</h2>
      <h4>Instructions:</h4>
      <p>
        <strong>Starting Position:</strong> Lie on your back with your knees bent and feet flat on the floor. Place your hands behind your head or crossed over your chest.<br>
        <strong>Raising Phase:</strong> Engage your core and lift your upper body off the ground while keeping your lower back pressed into the floor.<br>
        <strong>Lowering Phase:</strong> Slowly lower your upper body back to the starting position without letting your head touch the ground.<br>
        <strong>Repetitions:</strong> Aim for 3 sets of 12-20 repetitions.
      </p>
