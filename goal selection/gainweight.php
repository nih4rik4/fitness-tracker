      <h2>Bench Dips</h2>
      <h4>Instructions:</h4>
      <p>
        <strong>Starting Position:</strong> Sit on the edge of a bench or sturdy surface with your hands beside you, fingers pointing forward. Extend your legs in front of you, keeping your heels on the ground or elevated.<br>
        <strong>Lowering Phase:</strong> Shift your weight forward and lower your body by bending your elbows until they are at about a 90-degree angle.<br>
        <strong>Pushing Phase:</strong> Press through your palms to lift your body back up to the starting position.<br>
        <strong>Repetitions:</strong> Aim for 3 sets of 8-12 repetitions.
      </p>
